package com.lxisoft.animalgame.forest.animal;
public class Animal
{
	private String animalName;
	private int animalStrength;
	private int animalHunger;
	
	public void setanimalName(String animalName)
	{
		this.animalName=animalName;
	}
	public String getanimalName()
	{
		return animalName;
	}
	
	public void setanimalStrength(int animalStrength)
	{
		this.animalStrength=animalStrength;
	}
	
	public int getanimalStrength()
	{
		return animalStrength;
	}
	
	public void setanimalHunger(int animalHunger)
	{
		this.animalHunger=animalHunger;
	}
	
	public int getanimalHunger()
	{
		return animalHunger;
	}
}
	
		
	
	
	
		


