package com.lxisoft.actors;
public class Heroin
{
private  String heroinName,heroinCharactorName;
public Heroin(String heroinName,String heroinCharactorName)
{
	this.heroinName=heroinName;
	this.heroinCharactorName=heroinCharactorName;
}
public String getHeroinName()
{
	return heroinName;
}
public String getHeroinCharactorName()
{
	return heroinCharactorName;
}
}