package com.lxisoft.actors;
public class Heroine
{
private  String heroineName,heroineCharactorName;
public Heroine(String heroineName,String heroineCharactorName)
{
	this.heroineName=heroineName;
	this.heroineCharactorName=heroineCharactorName;
}
public String getHeroineName()
{
	return heroineName;
}
public String getHeroineCharactorName()
{
	return heroineCharactorName;
}
}