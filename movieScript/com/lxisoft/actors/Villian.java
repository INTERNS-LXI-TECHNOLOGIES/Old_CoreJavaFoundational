package com.lxisoft.actors;
public class Villian
{
private  String villianName,villianCharactorName;
public Villian(String villianName,String villianCharactorName)
{
	this.villianName=villianName;
	this.villianCharactorName=villianCharactorName;
}
public String getVillianName()
{
	return villianName;
}
public String getVillianCharactorName()
{
	return villianCharactorName;
}
}