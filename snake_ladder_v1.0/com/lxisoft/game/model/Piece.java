package com.lxisoft.game.model;
public class Piece
{
	private String color;
	private String shape;
	
	public void setColor(String color)
	{
		this.color=color;
	}
	
	public String getColor()
	{
		return color;
	}
	
	public void setShape(String shape)
	{
		this.shape=shape;
	}
	
	public String getShape()
	{
		return shape;
	}
	
}