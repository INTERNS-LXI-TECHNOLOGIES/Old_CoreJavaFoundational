package newanimalgame.com.lxisoft.animalgamev2.game.forest.animals;
public class Animal
{
	private String animalName,animalType;
	private int animalHungerLevel;
	private int animalEnergyLevel;
	
	public void setAnimalName(String name)
	{
		this.animalName=name;
	}
	
	public String getAnimalName()
	{
		return animalName;
	}
	
	public void setAnimalType(String type)
	{
		this.animalType=type;
	}
	
	public String getAnimalType()
	{
		return animalType;
	}
	
	public void setAnimalHungerLevel(int hunger)
	{
		this.animalHungerLevel=hunger;
	}
	
	public int getanimalHungerLevel()
	{
		return animalHungerLevel;
	}
	
	public void setAnimalEnergyLevel(int energy)
	{
		this.animalEnergyLevel=energy;
	}
	
	public int getAnimalEnergyLevel()
	{
		return animalEnergyLevel;
	}
	
}
		
	
		
	
		
	