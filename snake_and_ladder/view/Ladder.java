package com.lxisoft.snake_and_ladder.view;
public class Ladder
{
	private int top;
	private int bottom;
	public void setTop(int top)
	{
		this.top=top;
	}
	public int getTop()
	{
		return top;
	}
	public void setBottom(int bottom)
	{
		this.bottom=bottom;
	}
	public int getBottom()
	{
		return bottom;
	}
}